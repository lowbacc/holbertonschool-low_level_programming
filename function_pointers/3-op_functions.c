/**
 * op_add - Entry point
 * @a: integer
 * @b: integer
 * Return: Alwayx 0.
 */
int op_add(int a, int b)
{
    return (a + b);
}

/**
 * op_sub - Entry point
 * @a: integer
 * @b: integer
 * Return: Always 0.
 */
int op_sub(int a, int b)
{
    return (a - b);
}

/**
 * op_mul - Entry point
 * @a: integer
 * @b: integer
 * Return: Always 0.
 */
int op_mul(int a, int b)
{
    return (a * b);
}

/**
 * op_div - Entry point
 * @a: integer
 * @b: integer
 * Return: Always 0.
 */
int op_div(int a, int b)
{
    return (a / b);
}

/**
 * op_mod - Entry point
 * @a: integer
 * @b: integer
 * Return: Always 0.
 */
int op_mod(int a, int b)
{
    return (a % b);
}
